public class Tutorial19 {
    public static void main(String[] args) {
        int age = 45;

        if (age >= 60) {
            System.out.println("You are too old " + age);
        } else if (age >= 50) {
            System.out.println("You are in ur 50s " + age);
        } else if (age >= 40) {
            System.out.println("You are in ur 40s " + age);
        } else {
            System.out.println("You are young");
        }
    }
}
