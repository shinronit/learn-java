package JavaPlaylist2;

public class CodingExcercise {
    public static void PositiveNegativeZero(int number){
        if (number>0){
            System.out.println("positive");
        }else if (number<0){
            System.out.println("negative");
        }else if (number==0){
            System.out.println("zero");
        }
    }
}
