public class Tutorial22 {
    public static void main(String[]args) {
        // for is built in keyword it contains 3 statements inside it ,
        //1st where you want the loop to start
        // 2nd where the loop to end
        //third how much you want to increment by
        for (int counter=6;counter<=21;counter+=3){
            System.out.println(counter);
        }

    }
}
